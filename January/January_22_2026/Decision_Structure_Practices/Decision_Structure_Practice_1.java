public class Decision_Structure_Practice_1 {
    public static void main(String[] args) {
        int age = 18;
        if (age > 18) {
            System.out.println("Eligible");
        } else if (age == 18) {
            System.out.println("18 years old.");
        }
        else {
            System.out.println("Not Eligible");
        }
    }
}